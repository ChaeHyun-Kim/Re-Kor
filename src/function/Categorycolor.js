export const IconColor = (category) => {
  switch (category) {
    case "K-LANDSCAPE":
      return "#CFF09E";
    case "K-CULTURE":
      return "#D09E88";
    case "K-FOOD":
      return "#F26D5B";
    case "K-SHOPPING":
      return "#A3DAFF";
    case "K-POP":
      return "#A593E0";
    case "K-DRAMA":
      return "#FDD692";
    case "K-FESTIVAL":
      return "#F7AA97";
    case "K-ENTERTAINMENT":
      return "#52616A";
  }
};
